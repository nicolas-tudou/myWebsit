const getArticalDetail = (articalId) => {
  return new Promise((res, rej) => {
    articalList.forEach(ele => {
      if (ele.articalId === articalId) {
        res(ele)
      }
    })
    rej(new Error('this artical is not exist.'))
  })
}
const getArticalList = (typeInfo) => {
  return new Promise((res, rej) => {
    let list = []
    list = articalList.filter(ele => {
      ele.tag = (typeInfo && typeInfo.typeName) || articalSiderList[0].typeName
      return Math.random() > 0.7
    })
    res(list)
  })
}
const getObjectOptions = () => {
  let result = []
  articalSiderList.forEach(ele => {
    result.push({
      label: ele.typeName,
      value: ele.typeId
    })
  })
  return result
}
